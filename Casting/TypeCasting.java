package Casting;

public class TypeCasting {
    public static void main(String[] args) {
        int num = 129;
        double digit = num;
        System.out.println(digit);

       double dig = 123.873;
       int nu = (int)dig;
        System.out.println(nu);

        String n = "12345";
        int u = Integer.parseInt(n);
        System.out.println(u);
    }
}
